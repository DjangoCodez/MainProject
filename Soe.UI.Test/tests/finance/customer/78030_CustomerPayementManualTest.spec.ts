test.use({ account: { domain: process.env.defualt_domain!, user: 'adminERP' } });

test.beforeEach(async ({ page, accountEx, financeBasePage }) => {
    await allure.parentSuite("Finance");
    await allure.suite("Customer");
    await allure.subSuite("Customer Payments");
    await allure.link('https://dev.azure.com/softonedev/XE/_workitems/edit/' + testCaseId, "Test case : " + testCaseId);
    envUrl = accountEx.baseUrl;
    await financeBasePage.SetupFinance(envUrl, getAccountExValue(accountEx, 'role')?.toString() ?? 'Admin');
    invoiceNo_1 = testCaseId + Math.floor(100000 + Math.random() * 900000);
    invoiceUtil = new InvoiceUtil(page, envUrl);
    await invoiceUtil.CreateCustomerInvoice(invoiceNo_1);
});

test(testCaseId + ': Create Customer Payment Manual Payment : AP', { tag: ['@Finance', '@Customer', '@Regression'] }, async ({ financeBasePage, accountPayableSettingsPage, paymentsPage }) => {
    await financeBasePage.goToMenu('Settings_Finance', 'Settings', true, 'Accounts Payable');
    await accountPayableSettingsPage.waitForPageLoad();
    await accountPayableSettingsPage.transferInvoiceToVoucher();
    await accountPayableSettingsPage.unselectTransferPaymentToVoucher();
    await accountPayableSettingsPage.saveAccountPayableSettings();
    await financeBasePage.goToMenu('Customer', 'Payments');
    await paymentsPage.filterByInvoiceNumber(invoiceNo_1);
    await paymentsPage.registerNewPayment();
    await paymentsPage.selectPaymentMethod(paymentMethod);
    await paymentsPage.saveNewPayment();
    await paymentsPage.openChecking(true);
    await paymentsPage.filterByInvoiceNumberChecking(invoiceNo_1);
    await paymentsPage.edit(1);
    await paymentsPage.customerPaymentLoading();
    const paymentDueDate = await getDateUtil(30, true);
    await paymentsPage.enterPayementDate(paymentDueDate);
    await paymentsPage.saveAndCloseRegisterPayment(5, 1);
    await paymentsPage.openChecking(true);
    await paymentsPage.clearAllFilters(3);
    await paymentsPage.reload(1);
    await paymentsPage.filterByInvoiceNumberChecking(invoiceNo_1);
    await paymentsPage.edit();
    await paymentsPage.customerPaymentLoading();
    await paymentsPage.verifyPaymentDate(paymentDueDate);
});







































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































